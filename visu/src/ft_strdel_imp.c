#include "fillerh.h"

void	ft_strdel_imp(char ***as)
{
	int		x;

	x = 0;
	while (as[0][x])
	{
		ft_strdel(&(as[0][x]));
		x++;
	}
	free(as[0]);
	as[0] = NULL;
}
